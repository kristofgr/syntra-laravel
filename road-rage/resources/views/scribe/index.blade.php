        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include a <strong><code>X-XSRF-TOKEN</code></strong> header with the value <strong><code>"{YOUR_X_XSRF_TOKEN}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>You can retrieve your token by <b><a href="\login" target="_blank">logging in</a></b> with valid credentials, the XSRF-TOKEN will be present in the response header.</p>

